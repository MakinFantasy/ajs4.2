// test for dortByHealth func
import sortByHealth from '../sortByHealth';

test('sortByHealth test', () => {
  const characters = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const sortedByFunc = sortByHealth(characters);
  expect(sortedByFunc).toStrictEqual([
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ]);
});
